    void MarchCube(Vector3Int position, List<Vector3> verts, List<int> ind) {
		float[] cube = new float[8];
		for (int i = 0; i < 8; i++) {
			cube[i] = Point(position + CornerTable[i]);
		}

        int configIndex = GetCubeConfiguration(cube);
        if (configIndex == 0 || configIndex == 255)
            return;

        int edgeIndex = 0;
        for(int i = 0; i < 5; i++) {
            for (int p = 0; p < 3; p++) {
                int indice = TriangleTable[configIndex, edgeIndex];
                if (indice == -1)
                    return;

                Vector3 vert1 = position + CornerTable[EdgeIndexes[indice, 0]];
                Vector3 vert2 = position + CornerTable[EdgeIndexes[indice, 1]];

				Vector3 vertPosition;
				float vert1Sample = cube[EdgeIndexes[indice, 0]];
				float vert2Sample = cube[EdgeIndexes[indice, 1]];

				float difference = vert2Sample - vert1Sample;

				if (difference == 0) {
					difference = 0.5F;
				} else {
					difference = (0.5F - vert1Sample) / difference;
				}
				vertPosition = vert1 + ((vert2 - vert1) * difference);
				ind.Add(VerticeForIndice(vertPosition, position, verts));
                edgeIndex++;
            }
        }
    }

    int GetCubeConfiguration(float[] cube) {
        int configurationIndex = 0;
        for (int i = 0; i < 8; i++) {
            if (cube[i] > 0.5F) {
                configurationIndex |= 1 << i;
            }

        }
        return configurationIndex;

    }

	int VerticeForIndice(Vector3 vert, Vector3Int point, List<Vector3> verts) {
		vert = vert + new Vector3(x * terrain.chunkSize, 0, y * terrain.chunkSize);
		for (int i = 0; i < verts.Count; i++) {
			if (verts[i] == vert) {
				return i;
			}
		}
		verts.Add(vert);
		return verts.Count - 1;
	}

